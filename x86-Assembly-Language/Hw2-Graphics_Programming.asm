mov al,input ;bl中保存待显示字符的ASCII码
mov bx,010h ;十进制的16，显示一个字符所需要的字节数
mul bx ;将al中的ASCII码乘32换算起始位置，结果保存在ax中
sub ax,1 ;起始位置减一得到数组下标（从0开始）
mov si,ax ;si用作字模起始偏移地址
mov bp,0 ;bp用作总字节计数，共有16行要输出，有16*1=16个字节
mov bx,0 ;bx保存显存偏移地址

display:
mov di,1 ;di用作行字节输出计数,一行一个字节，如果字模数据形式变化，此处可修改

display_one_row:
mov dx,data
mov ds,dx ;数据段寄存器切换回显存；由于ax存有显示数据，不能用作段地址传递
mov ax,[si+bp] ;由于ds可能已经切换到显存所在逻辑段，还需要换回data段
mov cx,8 ;cx用作单字节画点计数

display_one_dot: ;画一个点
mov dx,0A000h 
mov ds,dx ;切换段寄存器到显存的逻辑段地址0A000h，便于后续操作，此时ax已被占用
shl ax,1
jc get_frontcolor ;最高位进入标志寄存器的CF，若CF为1则输出前景色（字体颜色）
mov byte ptr[bx],1 ;否则输出背景色，背景色为蓝色（1）
jmp dot_then ;防止输出背景色后输出前景色的指令被执行到
get_frontcolor:
mov byte ptr[bx],4 ;前景色为红色（4）
dot_then:
inc bx ;显存偏移地址加一
dec cx ;点计数减一
jnz display_one_dot ;未画完8个点则继续画

inc bp ;指向字模的下一个字节
dec di
jnz display_one_row ;画本行下一个字节数据

add bx,320-8 ;画完一行，显存偏移地址指向下一行最左侧
cmp bp,16-1 ;要画16行,16个字节
jbe display ;未画完所有行就接着画下一行

finish: ;绘图完成后的收尾工作
mov ah,0
int 16h ;调用16h BIOS中断等待一个键盘按键输入，使程序暂停以观察结果
mov ax,0003h ;切换回80*25字符模式，防止返回DOS后出现显示异常（ah=00h即Set Video Mode;al=03h即80*25字符模式）
int 10h
mov ah,4Ch ;调用21h DOS中断的4Ch功能返回DOS
int 21h

